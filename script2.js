function changeBorder() {
  // Get the image element by its ID
  const myImg = document.getElementById("logobutton");
  if (Autohide === null){
  // Change the border of the image element
  myImg.style.border = "2px solid  rgba(184, 4, 255, 0.966)";
  myImg.style.boxShadow = "-2px -1.5px 35px 2px   rgba(184, 4, 255, 0.966)"
  myImg.style.borderRadius = "35%"
  } else {
    myImg.removeAttribute('style');
  }
}

